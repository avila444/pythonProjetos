def soma(a: int, b: int) -> int:
    return a + b


def subt(a: int, b: int) -> int:
    return a - b


def mult(a: int, b: int) -> int:
    return a * b


def div(a: int, b: int) -> float:
    return a / b


def exp(a: int, b:int) -> int:
    return a ** b


def raiz(a: int, b: int) -> float:
    return a ** (1 / b)
